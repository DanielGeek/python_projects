"""
Faithfulness Test (Ragas)

Measures whether the response generated by the LLM is factually faithful to the retrieved context.
A high score indicates that the response does not contain information not present in the context
(i.e., no hallucinations).

Important:
- Faithfulness requires: user_input, response, retrieved_contexts
- Unlike Context Recall, it DOES NOT require a 'reference' (ground truth)
"""

import logging

import pytest

from helpers.llm_response import load_test_data
from ragas.metrics.collections import Faithfulness

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

# Path to the test data file for the Faithfulness metric
path_file_name = "faithfulness/selenium_faithfulness.json"


@pytest.mark.asyncio
@pytest.mark.parametrize("get_data", load_test_data(path_file_name), indirect=True)
async def test_faithfulness(ragas_llm, get_data):
    """
    Evaluates the Faithfulness metric to verify that the response
    generated by the RAG system remains faithful to the retrieved context.

    Required Ragas Parameters:
        user_input   -> user question
        response     -> LLM generated response
        retrieved_contexts -> documents retrieved from the vector store

    Unlike Context Recall, Faithfulness DOES NOT use 'reference'.
    """

    # Preliminary data validation
    if not get_data.response:
        pytest.fail("The LLM response is empty; Faithfulness cannot be evaluated.")

    if not get_data.retrieved_contexts:
        pytest.fail("No retrieved contexts; Faithfulness cannot be evaluated.")

    # Initialize Faithfulness metric with the LLM configured in conftest.py
    faithfulness = Faithfulness(llm=ragas_llm)

    # Calculate score asynchronously
    score = await faithfulness.ascore(
        user_input=get_data.user_input,
        response=get_data.response,
        retrieved_contexts=get_data.retrieved_contexts,
    )

    # Structured result logging
    logger.info(
        "Faithfulness score: %.4f | Question: %s",
        score.value,
        get_data.user_input,
    )

    # Validate minimum quality threshold (no hallucinations)
    assert score.value >= 0.8, (
        f"Faithfulness score {score.value:.4f} is lower than 0.8. "
        "The response contains information not present in the context (possible hallucination)."
    )
